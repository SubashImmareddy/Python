# REPL = Read, Eval, Print, Loop
# It is an interactive shell where Python executes code line by line.
# You usually open it by typing `python` in the terminal.

# Example: Printing multiplication table of 5 using REPL

# Start Python REPL (just type `python` in terminal)
# Then type this code:

for i in range(1, 11):     # loop from 1 to 10
    print(f"5 x {i} = {5*i}")   # print the multiplication result

